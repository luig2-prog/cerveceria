    private void txtCantiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantiKeyReleased
        // TODO add your handling code here:
        String d = txtCantidadDisponibleInformacion.getText();
        if (!d.equals("")) {
            try {
                txtCantidadSolicitada.setText(txtCanti.getText());
                int cantSoli = Integer.parseInt(txtCantidadSolicitada.getText());
                int cantDis = Integer.parseInt(txtCantidadDisponibleInformacion.getText());
                
                if (cantSoli <= cantDis) {
                    
                    int subVender = Integer.parseInt(txtPrecioVentaInformacion.getText()) * cantSoli;
                    txtSubtotalVenderInformacion.setText(subVender + "");
                    
                    jButton1.setEnabled(true);
                } else {
                    
                    JOptionPane.showMessageDialog(null, "La cantidad solicitada sobrepasa la cantidad en disponible");
                    jButton1.setEnabled(false);
                    txtCanti.setText("");
                }
            } catch (Exception e) {
                txtCanti.setText("");
                txtCantidadSolicitada.setText("");
            }
            
        }

    }//GEN-LAST:event_txtCantiKeyReleased

