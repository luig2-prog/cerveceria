    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dia = jComboBox2Dia2.getSelectedItem().toString(), mes = jComboBox3Mes2.getSelectedItem().toString(), año = jComboBox4Año2.getSelectedItem().toString();
        
        if (dia.equals("DIA") || mes.equals("MES") || año.equals("AÑO")) {
            JOptionPane.showMessageDialog(null, "Seleccione una fecha por favor");
            return;
        }
        
        fechaVenta = año + "-" + mes + "-" + dia;
        
        jComboBox2Dia2.setEnabled(false);
        jComboBox3Mes2.setEnabled(false);
        jComboBox4Año2.setEnabled(false);
        
        vendiendo = true;
        
        if (factura_Venta == 0) {
            
            factura_Venta = (int) Math.floor(Math.random() * (1000 - 9999 + 1) + 9999);  // Valor entre M y N, ambos incluidos.

            while (igualfactura(factura_Venta)) {
                factura_Venta = (int) Math.floor(Math.random() * (1000 - 9999 + 1) + 9999);  // Valor entre M y N, ambos incluidos.
                JOptionPane.showMessageDialog(null, "while" + factura_Venta);
            }
        }
        int id = Integer.parseInt(jTextField6.getText());
        
        for (int i = 0; i < 30; i++) {
            if (datosCompra[i].getIdproducto() == id) {
                JOptionPane.showMessageDialog(null, "Ya este producto ha sido registrado en la factura");
                return;
            }
        }
        
        DatosCompra da = new DatosCompra();
        
        da.setIdproducto(id);
        da.setCantS(Integer.parseInt(txtCantidadSolicitada.getText()));
        da.setCantD(Integer.parseInt(txtCantidadDisponibleInformacion.getText()));
        da.setPrecio_venta(Integer.parseInt(txtPrecioVentaInformacion.getText()));
        da.setNom(String.valueOf(jComboBox2.getSelectedItem()));
        da.setSub_total(Integer.parseInt(txtSubtotalVenderInformacion.getText()));
        totalventa += da.getSub_total();
        
        datosCompra[pos] = da;
        pos++;
        limpiar();
        jTextArea1.setText(facturaVenta());
        jButton9.setEnabled(true);
        jButton3.setEnabled(true);
        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
    }//GEN-LAST:event_jButton1ActionPerformed

