    void llenarTablaProductos() {
        String[] producto = new String[4];

        String sql = "SELECT * FROM producto";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            DefaultTableModel model = new DefaultTableModel();

            model.addColumn("ID PRODUCTO");
            model.addColumn("NOMBRE");
            model.addColumn("PRECIO VENTA");
            model.addColumn("PRECIO COMPRA");
            model.addColumn("CANTIDAD DISPONIBLE");
            while (rs.next()) {
                // Se crea un array que será una de las filas de la tabla.
                String[] fila = new String[5]; // Hay tres columnas en la tabla

                // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                for (int i = 0; i < 5; i++) {
                    fila[i] = String.valueOf(rs.getObject(i + 1)); // El primer indice en rs es el 1, no el cero, por eso se suma 1.
                }
                // Se añade al modelo la fila completa.
                model.addRow(fila);

            }
            jTable2.setModel(model);
        } catch (Exception e) {
        }
    }

